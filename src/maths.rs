pub mod maths {

    /// Adds two operands
    ///
    /// arg1: i32 operand
    /// arg2: i32 operand
    ///
    /// return i32
    ///
    pub fn add(arg1: i32, arg2: i32) -> i32 {
        return arg1 + arg2;
    }

    /// Substracts two operands
    ///
    /// arg1: i32 operand
    /// arg2: i32 operand
    ///
    /// return i32 arg1 - arg2
    ///
    pub fn sub(arg1: i32, arg2: i32) -> i32 {
        return arg1 - arg2;
    }
}
